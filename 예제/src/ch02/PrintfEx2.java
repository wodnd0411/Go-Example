package ch02;

class PrintfEx2 {
	
	public static void main(String[] args) {
		String url = "www.codechobo.com";

		float f1 = .10f;   // 0.10, 1.0e-1(1.0 x 10의 -1승)
		float f2 = 1e1f;   // 10.0, 1.0e1, 1.0e+1(1.0 x 10의 1승)
		float f3 = 3.14e3f;
		double d = 1.23456789;

		System.out.printf("f1 = %f, %e%n", f1, f1); 
		System.out.printf("f2 = %f, %e%n", f2, f2); 
		System.out.printf("f3 = %f, %e%n", f3, f3);

		System.out.printf("d = %f%n", d);
		System.out.printf("d = %14.10f%n", d); // 전체 14자리 중 소수점 아래 10자리

		System.out.printf("[%s]%n", url);
		System.out.printf("[%20s]%n", url); // 20칸 만들고 우측정렬
		System.out.printf("[%-20s]%n", url); // 20칸 만들고 좌측정렬
		System.out.printf("[%.8s]%n", url);  // 왼쪽에서 8글자만 출력
	}
	
}
